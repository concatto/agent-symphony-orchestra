package orchestra;


public class Conductor extends MusicianAgent{

	public Conductor() {
	}
	
	@Override
	protected void setup() {
		super.setup();
	}
	
	@Override
	public void play(int tone) {
		super.play(tone);
	}
	
	@Override
	public void stop(int tone) {
		super.stop(tone);
	}
	
	@Override
	public void beat(int index) {
		super.beat(index);
	}
}
